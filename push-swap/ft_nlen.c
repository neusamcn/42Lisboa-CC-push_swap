#include "push_swap.h"

int	ft_nlen(long long n)
{
	int	len;

	len = 0;
	if (n < 0)
	{
		len++;
		n *= -1;
	}
	if (n >= 0)
		len++;
	while (n > 9)
	{
		n /= 10;
		len++;
	}
	return (len);
}
